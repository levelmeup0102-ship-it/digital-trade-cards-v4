export function getRolePrompt(subCardId: string, roleCode: RoleCode | string | null): string {
  if (!roleCode) return '자기 직무 관점에서 인사이트를 작성하세요.';
  const cardPrompts = PROMPTS[subCardId];
  if (!cardPrompts) return '이 질문에 대해 자기 직무 관점에서 답해주세요.';
  return cardPrompts[roleCode as RoleCode] || '자기 직무 관점에서 인사이트를 작성하세요.';
}
